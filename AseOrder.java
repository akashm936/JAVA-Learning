class AseOrder
{
	public static void main(String[] args) {
		
        System.out.println("the Alphabet in UpperCase Acending Order");
		for(char ch='A';ch<='Z';ch++)
		{
			System.out.print(ch + " ");
		}
        
        System.out.println("the Alphabet in LowerCase Acending Order");
		for(char ch='a';ch<='z';ch++)
		{
			System.out.print(ch + " ");
		}
	}
}