class Keyword1
{
	public static void main(String[] args) {
		
		byte age = 20;
		short b = 5767;
		int c  =  37463;
		long d = 9348248432l;  //for very long no above 10 digit L is mendetory

		float e = 74.64f; //for float f at end is compersary
		double f = 7.567D;  //by defult double

		char g = 'A';  //single character

		boolean h = true;  //only true or false

		System.out.println("byte value is - " + age);
		System.out.println("short value is - " + b);
		System.out.println("Integer value is - " + c);
		System.out.println("Long value is - " + d);
		System.out.println("Float value is - " + e);
		System.out.println("Double value is - " + f);
		System.out.println("Character value is - " + g);
		System.out.println("Boolean value is - " + h);

	}
}