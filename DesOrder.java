class DesOrder
{
	public static void main(String[] args) {
		
        System.out.println("the Alphabet in UpperCase Decending Order");
		for(char ch='Z';ch>='A';ch--)
		{
			System.out.print(ch + " ");
		}
        
        System.out.println("the Alphabet in LowerCase Decending Order");
		for(char ch='z';ch>='a';ch--)
		{
			System.out.print(ch + " ");
		}
	}
}